    private void tfdPesquisaReservaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfdPesquisaReservaKeyTyped
        char vChar = evt.getKeyChar();
        if (rbnPesquisaReservaNome.isSelected()) {
            if (!(Character.isLetter(vChar) || (vChar == KeyEvent.VK_BACK_SPACE) || (vChar == KeyEvent.VK_DELETE))) {
                evt.consume();
            }
        } else if (rbnPesquisaReservaCPF.isSelected()) {
            if (!(Character.isDigit(vChar) || (vChar == KeyEvent.VK_BACK_SPACE) || (vChar == KeyEvent.VK_DELETE))) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_tfdPesquisaReservaKeyTyped

    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        if (tfdPesquisaReserva.getText().trim().isEmpty()) {
            reservaController.popularTabela(tblListaBusca, 3, "");
        } else if (rbnPesquisaReservaNome.isSelected()) {
            reservaController.popularTabela(tblListaBusca, 1, tfdPesquisaReserva.getText());
        } else if (rbnPesquisaReservaCPF.isSelected()) {
            reservaController.popularTabela(tblListaBusca, 2, tfdPesquisaReserva.getText());
        }
    }//GEN-LAST:event_btnPesquisaActionPerformed
    

