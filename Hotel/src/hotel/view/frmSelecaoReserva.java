    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (tblListaBusca.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Você não selecionou nenhuma reserva!");
        } else {
            reserva = ReservaRepository.readId(Integer.parseInt(tblListaBusca.getModel().getValueAt(tblListaBusca.getSelectedRow(), 0).toString()));
            this.dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

